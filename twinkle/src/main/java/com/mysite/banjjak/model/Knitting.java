package com.mysite.banjjak.model;

public class Knitting {

	private int knitId;
	private String knitTitle;
	private String knitContent;
	private String knitDate;
	private String knitRate;
	
	private String userId;
	private String nickname;
	
	private String knitUuid;
	private String knitFilename;
	public int getKnitId() {
		return knitId;
	}
	public void setKnitId(int knitId) {
		this.knitId = knitId;
	}
	public String getKnitTitle() {
		return knitTitle;
	}
	public void setKnitTitle(String knitTitle) {
		this.knitTitle = knitTitle;
	}
	public String getKnitContent() {
		return knitContent;
	}
	public void setKnitContent(String knitContent) {
		this.knitContent = knitContent;
	}
	public String getKnitDate() {
		return knitDate;
	}
	public void setKnitDate(String knitDate) {
		this.knitDate = knitDate;
	}
	public String getKnitRate() {
		return knitRate;
	}
	public void setKnitRate(String knitRate) {
		this.knitRate = knitRate;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getKnitUuid() {
		return knitUuid;
	}
	public void setKnitUuid(String knitUuid) {
		this.knitUuid = knitUuid;
	}
	public String getKnitFilename() {
		return knitFilename;
	}
	public void setKnitFilename(String knitFilename) {
		this.knitFilename = knitFilename;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	



	
}